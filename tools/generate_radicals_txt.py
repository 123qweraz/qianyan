ALL_RADS = list(set(RADICALS + MORE_RADICALS))

def generate():
    lines = []
    lines.append("SBSRF 笔画辅助码：部首偏旁与字母对应表 (TXT版)")
    lines.append("=" * 60)
    lines.append("编码规则:")
    lines.append("  1. 位于字首/左侧/上方 (Prefix): 使用该部首的【前两笔】映射字母 (辅助码第1位)")
    lines.append("  2. 位于字末/右侧/下方 (Suffix): 使用该部首的【末两笔】映射字母 (辅助码第2位)")
    lines.append("-" * 60)
    lines.append(f"{'部首':<4} | {'名称':<10} | {'常用位置':<8} | {'前两笔':<6} | {'首位码':<6} | {'末两笔':<6} | {'末位码':<6}")
    lines.append("-" * 60)

    # Sort by strokes length then strokes
    sorted_rads = sorted(ALL_RADS, key=lambda x: (len(x[2]), x[2]))

    for char, name, strokes, pos in sorted_rads:
        s12 = strokes[:2] if len(strokes) >= 2 else (strokes[0]*2 if len(strokes)==1 else "??")
        sn1n = strokes[-2:] if len(strokes) >= 2 else (strokes[0]*2 if len(strokes)==1 else "??")
        
        code1 = get_code(s12[0], s12[1])
        code2 = get_code(sn1n[0], sn1n[1])
        
        pos_str = "开头/结尾" if pos == 'B' else ("仅开头" if pos == 'P' else "仅结尾")
        
        # Determine which codes are applicable
        c1_display = code1 if pos in ['P', 'B'] else "-"
        c2_display = code2 if pos in ['S', 'B'] else "-"
        
        lines.append(f"{char:<4} | {name:<10} | {pos_str:<8} | {s12:<6} | {c1_display:<6} | {sn1n:<6} | {c2_display:<6}")

    with open('docs/stroke_radicals_map.txt', 'w', encoding='utf-8') as f:
        f.write('\n'.join(lines) + '\n')

if __name__ == "__main__":
    generate()
